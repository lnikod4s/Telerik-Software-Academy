// Write an expression that calculates rectangle's area by given 'width' and 'height'.

function solveTask(width, height) {
	var area = width * height;
	return 'The area of the rectangle is: ' + area;
}

console.log(solveTask(7, 8));
console.log(solveTask(67, 9));
console.log(solveTask(6.7, 8.9));
console.log(solveTask(745.78, 2736.89));