// Write script that asks for a digit and depending on the input shows the name of that digit (in English) using a
// switch statement.

function solveTask(digit) {
	switch (digit) {
		case 1:
			console.log('one');
			break;
		case 2:
			console.log('two');
			break;
		case 3:
			console.log('three');
			break;
		case 4:
			console.log('four');
			break;
		case 5:
			console.log('five');
			break;
		case 6:
			console.log('six');
			break;
		case 7:
			console.log('seven');
			break;
		case 8:
			console.log('eight');
			break;
		case 9:
			console.log('nine');
			break;
		default:
			console.log('zero');
			break;
	}
}

solveTask(0);
solveTask(1);
solveTask(2);
solveTask(3);
solveTask(4);
solveTask(5);
solveTask(6);
solveTask(7);
solveTask(8);
solveTask(9);